MENU_WITH_RECIPES_PROMPT_TEMPLATE = """
Жёсткие правила:
- Верни ТОЛЬКО валидный JSON. Никакого текста вне JSON.
- Вывод начинается с { и заканчивается }.
- Все ключи в двойных кавычках.
- Не добавляй и не удаляй ключи схемы.
- Используй ТОЛЬКО продукты из входного списка products.
- Запрещено добавлять "соль/перец/специи/вода" если их нет во входе.
- Если упоминаешь продукт в рецепте — укажи ссылку на продукт: product_id (предпочтительно) и product_name (строго как во входе).
- Никаких брендов.

Цель:
Собери недельное меню (7 дней × 3 приёма пищи) и ДЛЯ КАЖДОГО приёма пищи дай ПОДРОБНЫЙ рецепт.
Ориентир дневного калоража 2500 ккал/день (оценочно). Упор на белок/крупы/овощи/фрукты/молочку/масла.

Требования:
- В меню 21 слот (7 дней × 3): day_index 0..6, meal_index 0..2 (0=завтрак,1=обед,2=ужин)
- Для каждого слота должен быть recipe_key, и в recipes должен существовать рецепт с таким recipe_key
- В каждом рецепте 3–10 ингредиентов
- Каждая строка в menu_text обязана содержать 2–5 продуктов (не пусто)

Формат menu_text:
"День 1:
Завтрак: ...
Обед: ...
Ужин: ...
День 2:
..."

Схема ответа (строго):
{
  "meta": {
    "store": "Перекрёсток",
    "city": "Москва",
    "version": null
  },
  "menu_text": null,
  "menu_items": [
    {
      "day_index": null,
      "meal_index": null,
      "recipe_key": null
    }
  ],
  "recipes": [
    {
      "recipe_key": null,
      "name": null,
      "meal_type": null,
      "servings": null,
      "ingredients": [
        {
          "product_id": null,
          "product_name": null,
          "amount": null,
          "unit": null
        }
      ],
      "instructions": []
    }
  ]
}

Правила для unit в ингредиентах:
- unit строго из: ["g","kg","ml","l","pcs"]

Важно:
- recipe_key должен быть уникальным строковым ключом (например "d0_m0", "d0_m1", ... "d6_m2")
- meal_type: "breakfast" | "lunch" | "dinner"
- instructions: 3–7 коротких шагов (без лишней воды)

Входные данные:
{"products": __PRODUCTS_JSON__}
"""


def build_menu_text_prompt(products_json: str) -> str:
    return MENU_WITH_RECIPES_PROMPT_TEMPLATE.replace("__PRODUCTS_JSON__", products_json)
